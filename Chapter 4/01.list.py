friends = ["Apple", "Orange", 5, 354.06, False, "Aaksah", "Rohan"]

print(friends[0])        # Accessing the first element
print[0] = "Grapes"  # Unlike strings, lists are mutable

print(friends[0])       # Accessing the first element after modification
print(friends[1:4])     # Slicing the list from index 1 to 3