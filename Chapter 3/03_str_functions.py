name = "harry"

print(len(name))  # prints the length of the string 'name'
print(name.emdswith("rry"))  # checks if the string ends with 'ry'
print(name.startswith("ha"))  # checks if the string starts with 'ha'
print(name.capitalize())  # capitalizes the first letter of the string
print(name.find("ry"))  # finds the index of the substring 'ry'
print(name.replace("r", "p"))  # replaces all occurrences of 'r' with 'p'
print(name*3)  # prints the string 'name' three times